package ir.thebigbang.expandablerecyclerviewlibrary.models;

public class AttrItem {

    int id;
    String key;
    int value;

    public AttrItem(String key, int value) {
        this.key = key;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
